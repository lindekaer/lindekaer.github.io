import gulp from 'gulp'
import path from 'path'
import imagemin from 'gulp-imagemin'

/*
-----------------------------------------------------------------------------------
|
| Script tasks
|
-----------------------------------------------------------------------------------
*/

const imageTasks = {}

imageTasks.images = () => {
  gulp.src(path.join(__dirname, '..', 'src', 'images', '**', '*.*'))
    .pipe(imagemin())
    .pipe(gulp.dest(path.join(__dirname, '..', 'dist', 'images')))
}

/*
-----------------------------------------------------------------------------------
|
| Exports
|
-----------------------------------------------------------------------------------
*/

export default imageTasks
